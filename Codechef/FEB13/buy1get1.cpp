	while(t--){
		vector<int> alpha(52, 0);
		string s;
		cin>>s;
		for(int i=0; i<s.size(); i++){
			if (s[i] <= 'Z')
				alpha[s[i]-'A']++;
			else
				alpha[s[i]-'a'+26]++;
		}
		int cost = 0;
		for(int i=0; i<52; i++)
			cost += (alpha[i]+1)/2;
		cout<<cost<<endl;
	}
